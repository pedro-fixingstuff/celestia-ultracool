def min_teff(log_t: float) -> float:
    """Find the minimum value of temperature within the grid for a given age.
    """
    if log_t in t_values_unique:
        # Exact match: pick minimum Teff for the given age
        return 10 ** np.min(teff_values[t_values == log_t])
    else:
        # Get the two adjacent ages
        t_idx = np.searchsorted(t_values_unique, log_t)
        t_prev = t_values_unique[t_idx - 1]
        t_next = t_values_unique[t_idx]

        # Pick the minimum Teff for both ages
        min_teff_prev = np.min(teff_values[t_values == t_prev])
        min_teff_next = np.min(teff_values[t_values == t_next])

        # Return the maximum Teff out of the two minimums
        return 10 ** max(min_teff_prev, min_teff_next)


